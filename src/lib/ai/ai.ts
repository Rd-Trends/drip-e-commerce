const productSchema = z.object({
  title: z
    .string()
    .describe(
      'The product name/title. Detect brand and product line from images (logos, tags, embroidery, wordmarks).',
    ),

  costPriceInNGN: z
    .number()
    .nullable()
    .describe(
      'Cost price in Nigerian Naira as a plain number. This is the price the user posts. null if not mentioned.',
    ),

  sellingPriceInNGN: z
    .number()
    .nullable()
    .describe(
      'Selling price in Nigerian Naira. Calculated as costPriceInNGN × 1.4 (40% markup). null if cost price is null.',
    ),

  description: z
    .string()
    .describe('Product description for the storefront, derived from analyzing the product images.'),

  metaDescription: z
    .string()
    .describe('SEO meta description, 100–150 characters, keyword-rich, encourages clicks.'),

  isFeatured: z.boolean().describe('Whether the product should be featured on the storefront.'),

  inventory: z
    .number()
    .describe(
      'Stock quantity when variants are NOT enabled. Ignored when selectedVariants is not empty.',
    ),

  categories: z
    .array(
      z.object({
        name: z.string().describe('Category name'),
        id: z.number().describe('Category ID'),
      }),
    )
    .min(1)
    .max(3)
    .describe('1–3 categories from the provided list, ordered by relevance.'),

  images: z
    .array(
      z.object({
        id: z.number(),
        altText: z.string().describe('Alt text generated by analyzing the image content.'),
        imageName: z
          .string()
          .describe(
            'Descriptive kebab-case filename for the image (no extension). E.g. "nike-dri-fit-black-front-view".',
          ),
        colorVariantOptionId: z
          .number()
          .nullable()
          .describe(
            'If this image shows a specific color variant, the variant option ID from getVariantOptions results. null if not color-specific, no color variants, or the color is a newly created option (from newOptionLabels).',
          ),
      }),
    )
    .describe(
      'Images with IDs, alt text, name and optional color variant. Only include images actually present in the message.',
    ),

  productType: z
    .enum(['shirt', 'trousers', 'two-piece', 'other'])
    .describe('Product type classified from images and text.'),

  selectedVariants: z
    .array(
      z.object({
        variantTypeId: z
          .number()
          .describe('ID from getVariantTypes tool results. Never invent IDs.'),
        variantTypeName: z
          .string()
          .describe('Display name of the variant type, e.g. "Size". Copied from tool results.'),
        existingOptions: z
          .array(
            z.object({
              id: z.number().describe('ID from getVariantOptions results.'),
              label: z.string().describe('Option label, e.g. "XL". Copied from tool results.'),
            }),
          )
          .describe('Existing variant options picked directly from getVariantOptions results.'),
        newOptionLabels: z
          .array(z.string())
          .describe(
            'Custom option labels only when the user explicitly requests options not in the database.',
          ),
        inventoryPerOption: z
          .number()
          .describe('Inventory per variant option. Default 1 unless the user specifies otherwise.'),
      }),
    )
    .describe('Variant selections. Call getVariantTypes and getVariantOptions first.'),
})

